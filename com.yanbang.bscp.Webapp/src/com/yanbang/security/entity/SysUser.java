package com.yanbang.security.entity;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

/**
 * 系统用户实体
 * 
 * @author Tong Baojun
 * 
 */
@XmlRootElement(name = "SysUsers")
@XmlType(name = "SysUser")
@XmlAccessorType(XmlAccessType.FIELD)
public class SysUser implements Serializable {
	private static final long serialVersionUID = -6518541017469433092L;
	/**
	 * 流水号
	 */
	@XmlElement(name = "uuId")
	private Long uuId;
	/**
	 * 用户代码
	 */
	@XmlElement(name = "userCode")
	private String userCode;
	/**
	 * 用户呢称
	 */
	@XmlElement(name = "userNickName")
	private String userNickName;
	/**
	 * 用户名称
	 */
	@XmlElement(name = "userName")
	private String userName;
	/**
	 * 用户密码
	 */
	@XmlElement(name = "userPwd")
	private String userPwd;
	/**
	 * 用户类型(0=员工,1=客户)
	 */
	@XmlElement(name = "userType")
	private Integer userType;
	/**
	 * 创建日期(YYYY-MM-DD HH:MI:SS)
	 */
	@XmlElement(name = "userCreateDate")
	private String userCreateDate;
	/**
	 * 联系电话
	 */
	@XmlElement(name = "userTel")
	private String userTel;
	/**
	 * 联系邮箱
	 */
	@XmlElement(name = "userEMail")
	private String userEMail;
	/**
	 * 用户状态(0=有效,1=无效)
	 */
	@XmlElement(name = "userStatus")
	private Integer userStatus;
	/**
	 * 用户备注
	 */
	@XmlElement(name = "userRemark")
	private String userRemark;
	/**
	 * 数据来源编号（客户，员工UUID）
	 */
	@XmlElement(name = "userSourceCode")
	private String userSourceCode;

	public Long getUuId() {
		return uuId;
	}

	public void setUuId(Long uuId) {
		this.uuId = uuId;
	}

	public String getUserCode() {
		return userCode;
	}

	public void setUserCode(String userCode) {
		this.userCode = userCode;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserPwd() {
		return userPwd;
	}

	public void setUserPwd(String userPwd) {
		this.userPwd = userPwd;
	}

	public Integer getUserType() {
		return userType;
	}

	public void setUserType(Integer userType) {
		this.userType = userType;
	}

	public String getUserCreateDate() {
		return userCreateDate;
	}

	public void setUserCreateDate(String userCreateDate) {
		this.userCreateDate = userCreateDate;
	}

	public String getUserTel() {
		return userTel;
	}

	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}

	public String getUserEMail() {
		return userEMail;
	}

	public void setUserEMail(String userEMail) {
		this.userEMail = userEMail;
	}

	public Integer getUserStatus() {
		return userStatus;
	}

	public void setUserStatus(Integer userStatus) {
		this.userStatus = userStatus;
	}

	public String getUserRemark() {
		return userRemark;
	}

	public void setUserRemark(String userRemark) {
		this.userRemark = userRemark;
	}

	public String getUserNickName() {
		return userNickName;
	}

	public void setUserNickName(String userNickName) {
		this.userNickName = userNickName;
	}

	public String getUserSourceCode() {
		return userSourceCode;
	}

	public void setUserSourceCode(String userSourceCode) {
		this.userSourceCode = userSourceCode;
	}

}
